class BookModel {
  final String id;
  final String title;
  final String author;
  final double price;
  final int rating;
  final String image;
  final String description;
  final String category;

  BookModel({
    required this.id,
    required this.title,
    required this.author,
    required this.price,
    required this.rating,
    required this.image,
    required this.description,
    required this.category,
  });
}
